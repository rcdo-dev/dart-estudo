/**
 * Funções - Conceito
 * 
 * Conceito formal.
 * 
 * Bloco / agrupamento de código que possui um conjuntos de setenças que implementa o algoritmo,
 * onde é dado um nome para esse bloco de código que pode ser reusado a partir do nome que foi dado.
 * Uma função pode receber parâmetros de entrada e retornar algo em resposta a sua execução.
 * 
 * Conceito compreendido.
 * 
 * A ideia por trás de uma função está numa sequência de passos repetidas várias vezes no código,
 * podemos pegar esses passos e atribuir a uma função que será invocada pelo seu nome.
 * Funções podem receber ou não parâmetros e retornar ou não determinado valor.
 * 
 */
